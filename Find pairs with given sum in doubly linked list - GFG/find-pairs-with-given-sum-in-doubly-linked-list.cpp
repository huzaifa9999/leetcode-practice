class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        // code here
        
        vector<pair<int,int>>ans;
        Node *first=head;
        Node *second=head;
        
        while(second->next!=NULL)
        {
            second=second->next;
        }
        
        while(first!=second&&second->next!=first)
        {
            if(first->data+second->data==target)
            {
                ans.push_back(make_pair(first->data,second->data));
                first=first->next;
                second=second->prev;
            }
            else if(first->data+second->data<target) first=first->next;
            else second=second->prev;
        }
        
        return ans;
        
    }
};

